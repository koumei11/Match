// Personクラス
class Person {
  constructor(id, name, sex, age, height, address, image, hasVideo) {
    this.id = id;
    this.name = name;
    this.sex = sex;
    this.age = age;
    this.height = height;
    this.address = address;
    this.image = image;
    this.hasVideo = hasVideo;
  }
}

export default Person;
